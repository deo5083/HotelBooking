    //listeners
    private class DetailListener implements ActionListener{

        @Override
        public void actionPerformed(ActionEvent e) {
            int selectedRow = table.getSelectedRow();
            HotelTableView.this.parentController.setSelectedRow(selectedRow);
            if(!(selectedRow >= 0) ){
                System.out.println("User didnt select item.");
            } else {

                int selectedModelRow = table.convertRowIndexToModel(selectedRow);
                HotelTableView.this.parentController.getHotelDetailView(selectedModelRow);
            }
        }
    }
   
    
