    //others
    public void createTable(String[] hotels, String[] cities, String[] ratingsString){
        
        this.model = (DefaultTableModel) table.getModel();
        
        for(int i=0; i<hotels.length; i++){
            Object[] obj = new Object[]{hotels[i], cities[i], ratingsString[i]};
            model.addRow(obj);
        }
        
    }
    
